fn perform_coupon_redemption(
    dust_outpoint: &OutPoint,
    coupon_factory_id: &AlkaneId,
    block_height: u32
) -> Result<Block> {
    println!("\nüî• PERFORMING COUPON REDEMPTION OPERATION");
    println!("=========================================");
    println!("üîß Using DUST tokens from outpoint: {:?}", dust_outpoint);
    
    let dust_sheet = load_sheet(&RuneTable::for_protocol(AlkaneMessageContext::protocol_tag())
        .OUTPOINT_TO_RUNES.select(&consensus_encode(&dust_outpoint)?));
    let dust_token_rune_id = ProtoruneRuneId { block: 2, tx: 1 };
    let available_dust = dust_sheet.get(&dust_token_rune_id);
    
    println!("üîç Available DUST tokens: {}", available_dust);
    println!("üéØ Redeeming coupon token...");
    
    let redemption_block: Block = protorune_helpers::create_block_with_txs(vec![Transaction {
        version: Version::ONE,
        lock_time: bitcoin::absolute::LockTime::ZERO,
        input: vec![TxIn {
            previous_output: *dust_outpoint,
            script_sig: ScriptBuf::new(),
            sequence: Sequence::MAX,
            witness: Witness::new()
        }],
        output: vec![
            TxOut {
                script_pubkey: Address::from_str(ADDRESS1().as_str())
                    .unwrap()
                    .require_network(get_btc_network())
                    .unwrap()
                    .script_pubkey(),
                value: Amount::from_sat(546),
            },
            TxOut {
                script_pubkey: (Runestone {
                    edicts: vec![],
                    etching: None,
                    mint: None,
                    pointer: None,
                    protocol: Some(
                        vec![
                            Protostone {
                                message: into_cellpack(vec![
                                    coupon_factory_id.block,
                                    coupon_factory_id.tx,
                                    1u128, // redeem opcode
                                    1u128, // coupon_type (basic coupon)
                                ]).encipher(),
                                protocol_tag: AlkaneMessageContext::protocol_tag() as u128,
                                pointer: Some(0),
                                refund: Some(0),
                                from: None,
                                burn: None,
                                edicts: vec![
                                    ProtostoneEdict {
                                        id: ProtoruneRuneId {
                                            block: 2,
                                            tx: 1,
                                        },
                                        amount: 1000u128, // redemption_cost in DUST
                                        output: 1,
                                    }
                                ],
                            }
                        ].encipher()?
                    )
                }).encipher(),
                value: Amount::from_sat(546)
            }
        ],
    }]);
    index_block(&redemption_block, block_height)?;
    
    println!("‚úÖ Coupon redemption operation completed at block {}", block_height);
    
    // Analyze redemption results
    let redemption_outpoint = OutPoint {
        txid: redemption_block.txdata[0].compute_txid(),
        vout: 0,
    };
    
    let redemption_sheet = load_sheet(
        &RuneTable::for_protocol(AlkaneMessageContext::protocol_tag())
            .OUTPOINT_TO_RUNES
            .select(&consensus_encode(&redemption_outpoint)?)
    );
    
    println!("\nüîç REDEMPTION RESULTS ANALYSIS");
    println!("==============================");
    let mut coupon_found = false;
    let mut dust_change = 0u128;
    
    for (id, amount) in redemption_sheet.balances().iter() {
        println!("   ‚Ä¢ Token ID: {:?}, Amount: {}", id, amount);
        
        if id.block == 2 && id.tx == 1 {
            dust_change = *amount;
            println!("     ‚îî‚îÄ DUST change: {}", amount);
        } else {
            coupon_found = true;
            println!("     ‚îî‚îÄ üé´ COUPON TOKEN CREATED! Amount: {}", amount);
        }
    }
    
    if coupon_found {
        println!("üéä SUCCESS: Coupon token successfully redeemed!");
    } else {
        println!("‚ùå FAILURE: No coupon token found in redemption results");
    }
    
    println!("üí∞ DUST consumed: {}", available_dust.saturating_sub(dust_change));
    
    Ok(redemption_block)
}

